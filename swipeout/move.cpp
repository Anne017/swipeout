#include "move.h"

Move::Move(const int &id, const int &step, const int &delta):
    m_id(id),
    m_step(step),
    m_delta(delta)
{
}

int Move::id() const
{
    return m_id;
}

int Move::step() const
{
    return m_step;
}

int Move::delta() const
{
    return m_delta;
}

bool compareMove(const Move &move1, const Move &move2)
{
    return move1.step() < move2.step();
}
